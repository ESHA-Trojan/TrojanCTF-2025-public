#!/usr/bin/python
from itertools import cycle

flag = bytearray(
    [0xF8, 0xD9, 0xBF, 0x19, 0x8A, 0xDE, 0xD0, 0x85, 0xBE, 0x0, 0xBF, 0xD4, 0xC9, 0xB8, 0x14, 0xBF, 0x81, 0xD2, 0xFD, 0xA4, 0x17, 0x99, 0xEE, 0xD0, 0xD5, 0x8E, 0x8, 0x81, 0xC6, 0xD1, 0x8E, 0x16, 0xF, 0xBF, 0xC5, 0xCB, 0x8E, 0x10, 0x94, 0xD8, 0xC5, 0xFD, 0xA5, 0xD, 0x8F, 0xD9, 0xD6, 0xB8, 0xA8, 0xA, 0x81, 0xC3, 0xCD, 0xA1, 0x15, 0x85, 0x9D, 0xC8, 0xD0, 0xBE, 0x15, 0x85, 0xDC, 0xFD])

flag = bytearray([a ^ b for (a, b) in zip(flag, cycle(b'\x0f'))])

for i in range(8):
    for j in range(4):
        temp = flag[8 * i + j]
        flag[8 * i + j] = flag[8 * (i + 1) - (j + 1)]
        flag[8 * (i + 1) - (j + 1)] = temp

key = b'\xde\xad\xbe\xef\x77'

flag = bytearray([a ^ b for (a, b) in zip(flag, cycle(key))])

print(flag.decode())